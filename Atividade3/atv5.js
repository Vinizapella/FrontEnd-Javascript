// 5 - Desenvolva um algoritmo que leia 2 números inteiros e some. Se a soma for
// maior que 10, mostre o resultado da soma.
let n1 = parseInt(prompt("Q5: Digite o primeiro número inteiro:"));
let n2 = parseInt(prompt("Q5: Digite o segundo número inteiro:"));
let soma = n1 + n2;
if (soma > 10) {
  alert("A soma é: " + soma);
}